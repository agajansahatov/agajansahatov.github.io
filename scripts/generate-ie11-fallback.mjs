import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const rootDir = join(__dirname, '..');
const translationsPath = join(rootDir, 'src/i18n/ie11FallbackTranslations.json');
const outputPath = join(rootDir, 'public/ie11-fallback.js');

const translations = JSON.parse(readFileSync(translationsPath, 'utf8'));
const { languageOptions, ...localeTranslations } = translations;
const supportedLanguages = Object.keys(localeTranslations);

const output = `/**
 * Internet Explorer (Trident) cannot run this app: React 19, ES modules, and
 * modern APIs are unsupported. This script only runs in IE and replaces #root
 * with a static, readable fallback (ES5 for IE11 parsing).
 *
 * Generated by scripts/generate-ie11-fallback.mjs — do not edit by hand.
 */
(function () {
	'use strict';

	if (!document.documentMode) {
		return;
	}

	var STORAGE_KEY = 'agajansahatovportfolio:language';
	var DEFAULT_LANGUAGE = 'en';
	var SUPPORTED_LANGUAGES = ${JSON.stringify(supportedLanguages)};
	var LANGUAGE_OPTIONS = ${JSON.stringify(languageOptions)};
	var TRANSLATIONS = ${JSON.stringify(localeTranslations, null, '\t')};

	var root = document.getElementById('root');
	if (!root) {
		return;
	}

	var link = document.createElement('link');
	link.rel = 'stylesheet';
	link.href = '/ie11-fallback.css';
	document.head.appendChild(link);

	function escapeHtml(value) {
		return String(value)
			.replace(/&/g, '&amp;')
			.replace(/</g, '&lt;')
			.replace(/>/g, '&gt;')
			.replace(/"/g, '&quot;')
			.replace(/'/g, '&#39;');
	}

	function isSupportedLanguage(code) {
		return SUPPORTED_LANGUAGES.indexOf(code) !== -1;
	}

	function resolveBrowserLanguage() {
		var candidates = [];

		if (navigator.languages && navigator.languages.length > 0) {
			candidates = navigator.languages;
		} else if (navigator.language) {
			candidates = [navigator.language];
		}

		for (var index = 0; index < candidates.length; index += 1) {
			var normalized = String(candidates[index]).toLowerCase();

			for (
				var languageIndex = 0;
				languageIndex < SUPPORTED_LANGUAGES.length;
				languageIndex += 1
			) {
				var languageCode = SUPPORTED_LANGUAGES[languageIndex];
				if (normalized.indexOf(languageCode) === 0) {
					return languageCode;
				}
			}
		}

		return null;
	}

	function resolveLanguage() {
		var storedLanguage = null;

		try {
			storedLanguage = localStorage.getItem(STORAGE_KEY);
		} catch (error) {
			storedLanguage = null;
		}

		if (storedLanguage && isSupportedLanguage(storedLanguage)) {
			return storedLanguage;
		}

		return resolveBrowserLanguage() || DEFAULT_LANGUAGE;
	}

	function persistLanguage(languageCode) {
		try {
			localStorage.setItem(STORAGE_KEY, languageCode);
		} catch (error) {
			// Ignore storage failures in restricted environments.
		}
	}

	function buildLanguageSwitcher(activeLanguage) {
		var items = [];

		for (var index = 0; index < SUPPORTED_LANGUAGES.length; index += 1) {
			var languageCode = SUPPORTED_LANGUAGES[index];
			var isActive = languageCode === activeLanguage;
			var className = isActive
				? 'ie11-fallback-lang-option is-active'
				: 'ie11-fallback-lang-option';
			var ariaCurrent = isActive ? ' aria-current="true"' : '';

			items.push(
				'<li><button type="button" class="' +
					className +
					'" data-language="' +
					escapeHtml(languageCode) +
					'"' +
					ariaCurrent +
					'>' +
					escapeHtml(LANGUAGE_OPTIONS[languageCode]) +
					'</button></li>',
			);
		}

		return (
			'<div class="ie11-fallback-lang">' +
			'<p class="ie11-fallback-lang-label">' +
			escapeHtml(TRANSLATIONS[activeLanguage].languageLabel) +
			'</p>' +
			'<ul class="ie11-fallback-lang-list">' +
			items.join('') +
			'</ul>' +
			'</div>'
		);
	}

	function render(languageCode) {
		var copy = TRANSLATIONS[languageCode] || TRANSLATIONS[DEFAULT_LANGUAGE];

		document.documentElement.lang = languageCode;
		document.title = copy.pageTitle;

		root.className = 'ie11-fallback-root';
		root.innerHTML =
			'<main class="ie11-fallback-main" role="main">' +
			buildLanguageSwitcher(languageCode) +
			'<h1 class="ie11-fallback-title">' +
			escapeHtml(copy.title) +
			'</h1>' +
			'<p class="ie11-fallback-lead">' +
			escapeHtml(copy.lead) +
			'</p>' +
			'<ul class="ie11-fallback-list">' +
			'<li>' +
			escapeHtml(copy.browsers.edge) +
			'</li>' +
			'<li>' +
			escapeHtml(copy.browsers.chromeFirefox) +
			'</li>' +
			'<li>' +
			escapeHtml(copy.browsers.safari) +
			'</li>' +
			'</ul>' +
			'<p class="ie11-fallback-actions">' +
			'<a href="https://www.microsoft.com/edge" target="_blank" rel="noopener noreferrer">' +
			escapeHtml(copy.getEdge) +
			'</a>' +
			'<a href="https://browsehappy.com/" target="_blank" rel="noopener noreferrer">' +
			escapeHtml(copy.browseHappy) +
			'</a>' +
			'</p>' +
			'<p class="ie11-fallback-footnote">' +
			escapeHtml(copy.footnote) +
			'</p>' +
			'</main>';

		var buttons = root.getElementsByTagName('button');
		for (var buttonIndex = 0; buttonIndex < buttons.length; buttonIndex += 1) {
			var button = buttons[buttonIndex];
			if (!button.getAttribute('data-language')) {
				continue;
			}

			button.onclick = (function (nextLanguage) {
				return function () {
					if (nextLanguage === languageCode) {
						return;
					}

					persistLanguage(nextLanguage);
					render(nextLanguage);
				};
			})(button.getAttribute('data-language'));
		}
	}

	render(resolveLanguage());
})();
`;

writeFileSync(outputPath, output, 'utf8');
console.log(`Wrote ${outputPath}`);
