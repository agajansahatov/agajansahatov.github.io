const group = (
	id: string,
	labelKey: ExpertiseLabelKey,
	children: readonly SkillGraphNode[],
): SkillGraphGroupNode => ({
	kind: 'group',
	id,
	labelKey,
	children,
});

const technology = (
	id: ExpertiseItemId,
	label: string,
	icon: ExpertiseCatalogItem['icon'],
	officialUrl: string,
): ExpertiseCatalogItem => ({
	id,
	kind: 'technology',
	label,
	icon,
	officialUrl,
	iconColor: resolveExpertiseIconColor(id),
});

const concept = (
	id: ExpertiseItemId,
	labelKey: ExpertiseLabelKey,
	icon: ExpertiseCatalogItem['icon'],
): ExpertiseCatalogItem => ({
	id,
	kind: 'concept',
	labelKey,
	icon,
});

